package studentManage;

/**
 *
 * @author Administrator
 */
public class Student {
    private String studentNumber;
    private String studentName;
    private String studentSex;
    private String studentAge;
    private String studentWeight;

    /**
     * @return the studentNumber
     */
    public String getStudentNumber() {
        return studentNumber;
    }

    /**
     * @param studentNumber the studentNumber to set
     */
    public void setStudentNumber(String studentNumber) {
        this.studentNumber = studentNumber;
    }

    /**
     * @return the studentName
     */
    public String getStudentName() {
        return studentName;
    }

    /**
     * @param studentName the studentName to set
     */
    public void setStudentName(String studentName) {
        this.studentName = studentName;
    }

    /**
     * @return the studentSex
     */
    public String getStudentSex() {
        return studentSex;
    }

    /**
     * @param studentSex the studentSex to set
     */
    public void setStudentSex(String studentSex) {
        this.studentSex = studentSex;
    }

    /**
     * @return the studentAge
     */
    public String getStudentAge() {
        return studentAge;
    }

    /**
     * @param studentAge the studentAge to set
     */
    public void setStudentAge(String studentAge) {
        this.studentAge = studentAge;
    }

    /**
     * @return the studentWeight
     */
    public String getStudentWeight() {
        return studentWeight;
    }

    /**
     * @param studentWeight the studentWeight to set
     */
    public void setStudentWeight(String studentWeight) {
        this.studentWeight = studentWeight;
    }
    
}
